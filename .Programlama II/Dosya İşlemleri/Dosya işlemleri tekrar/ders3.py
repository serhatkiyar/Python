# Dosya açma işlemlerinde kullanılan parametreler.

# open() fonksiyonu ikinci aldığı parametreler dosyayı açma şeklimizi belirtir:
#                       'w'    'r'    'r+'    'a'

# "w" Mevcut bir dosya açılırsa, dosyadaki tüm içerik silinir ve yeni içerik yazılır.
#     Dosya mevcut değilse, yeni bir dosya oluşturulur.

# "r": Dosyayı okuma modunda açar.

# "a": Dosyayı ekleme modunda açar. Bu modda, dosyanın sonuna yeni içerik ekleyebilirsiniz.

# "r+": Dosyayı okuma ve yazma modunda açar.


# Dosya açma işlemlerinde kullanılan fonksiyonlar.

# file.seek()   file.tell()    file.read()    file.write()    open()    file.close()


# file.seek() :  İmlecimizin yerini değiştirir.

# file.tell() :  İmlecimizin bulunduğu indexi söyler.

# file..read() :  Dosya içeriğimizi string halinde verir.

# file.write() :  Dosyaya imlecin bulunduğu indexten başlayarak içine parametre olarak aldığı stringi yazar.

# open() :  Dosya açar.
# Birinci parametresi dosyanın dizin yolu.
# İkinci parametresi dosyanın açılma şekli.

# file.close() :  file dosyasını kapatır.

